    private void BotaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoCadastrarActionPerformed
       nome = CampoNomeCliente.getText();
       endereco = CampoEnderecoCliente.getText();
       CPF = CampoCPFCliente.getText();
       telefone = CampoTelefoneCliente.getText();
       
       Cliente c = new Cliente(nome , CPF, telefone, endereco, 0);
       
       aux = c.cadastrarCliente(c);
       if(aux == true){
           JOptionPane.showMessageDialog(rootPane, "Cliente cadastrado com sucesso!");
       }
       else{
           JOptionPane.showMessageDialog(rootPane, "Dados invalidos");
       }
    }//GEN-LAST:event_BotaoCadastrarActionPerformed

